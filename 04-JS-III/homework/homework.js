function numeroMasGrande(numeros) {
  // "numeros" debe ser una matriz de enteros (int/integers)
  // Devuelve el número más grande
  // Tu código:
  const ordenarMayoraMenor = (firstItem, secondItem) => secondItem - firstItem;
  let arregloOrdenado = [...numeros.sort(ordenarMayoraMenor)];
  return arregloOrdenado[0];
}

function multiplicarArgumentos() {
  // Usa la palabra clave `arguments` para multiplicar todos los argumentos y devolver el producto
  // Si no se pasan argumentos devuelve 0. Si se pasa un argumento, simplemente devuélvelo
  // Escribe tu código aquí:
  let arregloArgumentos = [...arguments];
  const multiplicar = (prevValue, currValue) => prevValue * currValue;
  let resultado = 0;
  if (arregloArgumentos.length > 1) {
    resultado = arregloArgumentos.reduce(multiplicar);
  } else if (arregloArgumentos.length === 1) {
    resultado = arregloArgumentos[0];
  }
  return resultado;
}

